def ipl_season_intro_html(s):
    season = s["season"]
    bio = IPL_SEASON_BIOS.get(season)
    if bio:
        return prose(bio, heading="सीज़न की कहानी")
    # auto narrative
    champ = s.get("champion")
    tr = s.get("top_run") or {}
    tw = s.get("top_wkt") or {}
    parts = []
    if champ:
        parts.append(f"आईपीएल {season} का ख़िताब {team_hi(champ)} ने जीता।")
    else:
        parts.append(f"आईपीएल {season} सीज़न में कुल {s.get('matches','—')} मैच खेले गए।")
    if tr.get("name"):
        parts.append(f"इस सीज़न में सबसे ज़्यादा रन {tr['name']} ने बनाए "
                     f"({tr.get('runs','—')} रन) और उन्हें ऑरेंज कैप मिली।")
    if tw.get("name"):
        parts.append(f"सर्वाधिक विकेट {tw['name']} ने लिए "
                     f"({tw.get('wkts','—')} विकेट) और पर्पल कैप उनके नाम रही।")
    return prose(" ".join(parts), heading="सीज़न की कहानी")


