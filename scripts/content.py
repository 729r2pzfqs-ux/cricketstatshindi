def _auto_team_paragraph(t):
    name_hi = team_hi(t["name"])
    fmts = ", ".join(FMT[f][0] for f in t["formats"])
    is_ipl = list(t["formats"].keys()) == ["IPL"]
    kind = "आईपीएल फ़्रेंचाइज़ी" if is_ipl else "क्रिकेट टीम"
    s1 = (f"{name_hi} एक {kind} है जिसने {fmts} में कुल {t['total_played']} मैच खेले हैं।")
    s2 = (f"इनमें से टीम ने {t['total_won']} मुक़ाबले जीते हैं, यानी कुल जीत प्रतिशत "
          f"{t['win_pct']}% रहा है।")
    return s1 + " " + s2


def team_intro_html(t):
    bio = TEAM_BIOS.get(t["name"])
    if bio:
        return prose(bio, heading="परिचय")
    return prose(_auto_team_paragraph(t), heading="परिचय")


