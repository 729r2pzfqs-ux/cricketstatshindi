# Shared prose box styling
# ---------------------------------------------------------------------------
def prose(paras, heading=None):
    """Wrap one Hindi paragraph (str) or several (list) in a styled card."""
    if isinstance(paras, str):
        paras = [paras]
    h = (f'<h2 class="hi font-heading font-bold text-lg text-cr-ink mb-3 '
         f'flex items-center gap-2"><span class="w-1.5 h-5 rounded pitch-stripe '
         f'inline-block"></span>{heading}</h2>') if heading else ""
    body = "".join(f'<p class="mb-3 last:mb-0">{p}</p>' for p in paras)
    return (f'<section class="bg-cr-card border border-cr-border rounded-2xl '
            f'p-5 sm:p-6 mb-6">{h}<div class="hi text-[15px] leading-relaxed '
            f'text-cr-text">{body}</div></section>')


