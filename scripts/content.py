def _player_role(p):
    r, w = p["total_runs"], p["total_wkts"]
    if r >= 3000 and w >= 100:
        return "ऑलराउंडर"
    if w >= 100 and r < w * 12:
        return "गेंदबाज़"
    if w >= 150:
        return "ऑलराउंडर"
    return "बल्लेबाज़"


def _best_bat(p):
    best, hi = None, -1
    for f, b in p["bat"].items():
        if b["runs"] > hi:
            hi, best = b["runs"], f
    return best


def _best_bowl(p):
    best, hi = None, -1
    for f, b in p["bowl"].items():
        if b["wkts"] > hi:
            hi, best = b["wkts"], f
    return best


def _auto_player_paragraph(p, hindi_name=None):
    name = hindi_name or p["name"]
    role = _player_role(p)
    teams = p.get("teams") or []
    team_part = ""
    if teams:
        team_part = f"{team_hi(teams[0])} के लिए खेलने वाले "
    fmts = ", ".join(FMT[f][0] for f in p["formats"])
    s1 = (f"{name} {team_part}एक {role} हैं जिन्होंने {fmts} में अपने आँकड़े दर्ज "
          f"किए हैं।")
    # career totals
    bits = []
    if p["total_runs"] > 0:
        bits.append(f"{p['total_runs']:,} रन")
    if p["total_wkts"] > 0:
        bits.append(f"{p['total_wkts']} विकेट")
    tot = " और ".join(bits) if bits else "अपना योगदान"
    s2 = f"सभी प्रारूपों को मिलाकर उन्होंने {p['total_m']} मैचों में {tot} दर्ज किए हैं।"
    # format highlight
    s3 = ""
    if role == "गेंदबाज़":
        bf = _best_bowl(p)
        if bf:
            b = p["bowl"][bf]
            extra = f", सर्वश्रेष्ठ {b['bbi']}" if b.get("bbi") and b["bbi"] != "0/0" else ""
            s3 = f"{FMT[bf][0]} में उनके नाम {b['wkts']} विकेट{extra} दर्ज हैं।"
    else:
        bf = _best_bat(p)
        if bf:
            b = p["bat"][bf]
            tons = []
            if b.get("hundreds"):
                tons.append(f"{b['hundreds']} शतक")
            if b.get("fifties"):
                tons.append(f"{b['fifties']} अर्धशतक")
            tpart = f" ({', '.join(tons)})" if tons else ""
            avg = f", औसत {b['avg']}" if b.get("avg") else ""
            s3 = f"{FMT[bf][0]} उनका सबसे सफल प्रारूप रहा है जहाँ उन्होंने {b['runs']:,} रन{tpart}{avg} बनाए।"
    return " ".join(x for x in (s1, s2, s3) if x)


def player_intro_html(p, hindi_name=None):
    """Return a styled Hindi intro card for a player page."""
    bio = PLAYER_BIOS.get(p["name"])
    if bio:
        return prose(bio, heading="परिचय")
    return prose(_auto_player_paragraph(p, hindi_name), heading="परिचय")


